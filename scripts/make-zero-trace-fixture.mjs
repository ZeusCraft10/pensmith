// scripts/make-zero-trace-fixture.mjs — deterministic generator for the
// committed tests/fixtures/sample-zero-trace.docx negative-control fixture
// (Phase 6 Wave 0, Plan 06-01 Task 1).
//
// PURPOSE
// -------
// Produces a REAL minimal DOCX ZIP that the zero-trace test (TEST-10 / DONE-07)
// scans as a negative control. The fixture deliberately carries identifying
// metadata across the FULL Dublin-Core + cp field set AND embeds the literal
// 'pensmith' in a docProps field a too-narrow patch would miss (cp:category)
// AND in a NON-.xml entry (_rels/.rels). A zeroTracePatch() that is too narrow
// (only blanks dc:creator/dc:title, only sweeps *.xml) MUST fail against this
// fixture — that is the whole point of an independently-authored negative control.
//
// The XML below is HAND-AUTHORED. It is NOT a mirror of the patch's regex
// source — it varies tag forms (a self-closing <Template/>, an attribute-bearing
// <dc:subject xml:lang="en">) precisely so the patch must be tolerant rather
// than overfit to one shape.
//
// DETERMINISM (06-RESEARCH Pitfall 6)
// -----------------------------------
// JSZip stamps each entry with Date.now() by default. We pass { date: new Date(0) }
// to every zip.file() call AND generate with platform 'UNIX' + a fixed compression
// so re-running this script on any machine/OS yields a byte-identical .docx. That
// stability is what lets tests/repo-files.test.ts SHA-256 byte-pin the fixture.
//
// REGENERATE: node scripts/make-zero-trace-fixture.mjs
// (after any intentional change, re-pin the SHA-256 in tests/repo-files.test.ts)

import JSZip from 'jszip';
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT = join(__dirname, '..', 'tests', 'fixtures', 'sample-zero-trace.docx');

// Fixed epoch for every ZIP entry → byte-deterministic output.
const EPOCH = new Date(0);

// --- [Content_Types].xml (well-formed; no 'pensmith') ---
const CONTENT_TYPES = `<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<Types xmlns="http://schemas.openxmlformats.org/package/2006/content-types">
  <Default Extension="rels" ContentType="application/vnd.openxmlformats-package.relationships+xml"/>
  <Default Extension="xml" ContentType="application/xml"/>
  <Override PartName="/word/document.xml" ContentType="application/vnd.openxmlformats-officedocument.wordprocessingml.document.main+xml"/>
  <Override PartName="/docProps/core.xml" ContentType="application/vnd.openxmlformats-package.core-properties+xml"/>
  <Override PartName="/docProps/app.xml" ContentType="application/vnd.openxmlformats-officedocument.extended-properties+xml"/>
</Types>
`;

// --- _rels/.rels — a NON-.xml-extension entry carrying the literal 'pensmith'.
// A .xml-only sweep would MISS this. The trap that fails a too-narrow patch.
const RELS = `<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<!-- generated by pensmith fixture authoring; the literal token below is the negative-control trap -->
<Relationships xmlns="http://schemas.openxmlformats.org/package/2006/relationships">
  <Relationship Id="rId1" Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/officeDocument" Target="word/document.xml"/>
  <Relationship Id="rId2" Type="http://schemas.openxmlformats.org/package/2006/relationships/metadata/core-properties" Target="docProps/core.xml"/>
  <Relationship Id="rId3" Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/extended-properties" Target="docProps/app.xml"/>
</Relationships>
`;

// --- docProps/core.xml — FULL Dublin-Core + cp identifying set, each NON-EMPTY.
// 'pensmith' appears in dc:title AND cp:category. dc:subject is ATTRIBUTE-bearing.
// Timestamps are real (non-epoch) so the patch's epoch-normalization is testable.
const CORE = `<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<cp:coreProperties xmlns:cp="http://schemas.openxmlformats.org/package/2006/metadata/core-properties" xmlns:dc="http://purl.org/dc/elements/1.1/" xmlns:dcterms="http://purl.org/dc/terms/" xmlns:dcmitype="http://purl.org/dc/dcmitype/" xmlns:xsi="http://www.w3.org/2001/XMLSchema-instance">
  <dc:creator>Trace Sentinel</dc:creator>
  <dc:title>pensmith draft</dc:title>
  <dc:subject xml:lang="en">Trace Subject</dc:subject>
  <dc:description>Trace Description</dc:description>
  <cp:keywords>trace, sentinel</cp:keywords>
  <cp:category>pensmith-category</cp:category>
  <cp:contentStatus>Draft</cp:contentStatus>
  <cp:lastModifiedBy>Trace Sentinel</cp:lastModifiedBy>
  <dcterms:created xsi:type="dcterms:W3CDTF">2026-06-18T12:00:00Z</dcterms:created>
  <dcterms:modified xsi:type="dcterms:W3CDTF">2026-06-18T12:00:00Z</dcterms:modified>
</cp:coreProperties>
`;

// --- docProps/app.xml — Application + Company + Manager + a SELF-CLOSING Template.
// The self-closing tag proves the patch must handle that form too.
const APP = `<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<Properties xmlns="http://schemas.openxmlformats.org/officeDocument/2006/extended-properties" xmlns:vt="http://schemas.openxmlformats.org/officeDocument/2006/docPropsVTypes">
  <Application>Microsoft Word 12.0.0</Application>
  <Company>Pensmith Test Co</Company>
  <Manager>Trace Manager</Manager>
  <Template/>
  <AppVersion>12.0000</AppVersion>
</Properties>
`;

// --- word/document.xml — trivial well-formed body; NO 'pensmith'.
const DOCUMENT = `<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<w:document xmlns:w="http://schemas.openxmlformats.org/wordprocessingml/2006/main">
  <w:body>
    <w:p><w:r><w:t>Sample body text with no identifying trace.</w:t></w:r></w:p>
  </w:body>
</w:document>
`;

async function main() {
  const zip = new JSZip();
  zip.file('[Content_Types].xml', CONTENT_TYPES, { date: EPOCH });
  zip.file('_rels/.rels', RELS, { date: EPOCH });
  zip.file('docProps/core.xml', CORE, { date: EPOCH });
  zip.file('docProps/app.xml', APP, { date: EPOCH });
  zip.file('word/document.xml', DOCUMENT, { date: EPOCH });

  const buf = await zip.generateAsync({
    type: 'nodebuffer',
    platform: 'UNIX',
    compression: 'DEFLATE',
    compressionOptions: { level: 6 },
  });
  writeFileSync(OUT, buf);
  process.stdout.write(`wrote ${OUT} (${buf.length} bytes)\n`);
}

main().catch((err) => {
  process.stderr.write(`make-zero-trace-fixture failed: ${String(err)}\n`);
  process.exit(1);
});
